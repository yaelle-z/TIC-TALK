def benchmark_models_and_segmentations(cfg: Config, device: str) -> pd.DataFrame:
    rows = []

    for emb_name in cfg.embedding_models:
        print(f"\n=== Modèle: {emb_name} | device={device} ===")
        for bs in cfg.block_sizes_seconds:
            df = load_corpus_with_provenance(cfg.input_srt_dir, block_size_sec=bs)
            docs = df["text"].tolist()

            run_topics, run_outliers, run_n_topics, run_diversities = [], [], [], []

            for r in range(cfg.benchmark_runs):
                set_all_seeds(cfg.random_state + r)

                model, topics, probs = fit_with_outlier_reduction(
                    docs, cfg, embedding_model_name=emb_name, run_id=r, device=device
                )

                run_topics.append(topics)
                run_outliers.append(outlier_ratio(topics))
                run_n_topics.append(n_topics_effective(topics))
                run_diversities.append(approx_topic_diversity(model, topk=10))

                # Nettoyage mémoire
                del model, probs
                gc.collect()
                if torch.cuda.is_available():
                    torch.cuda.empty_cache()

            stab = stability_metrics(run_topics)

            rows.append({
                "embedding_model": emb_name,
                "block_size_sec": bs,
                "n_docs": len(docs),
                "outlier_ratio_mean": float(np.mean(run_outliers)),
                "outlier_ratio_std": float(np.std(run_outliers)),
                "n_topics_mean": float(np.mean(run_n_topics)),
                "n_topics_std": float(np.std(run_n_topics)),
                "topic_diversity_mean": float(np.mean(run_diversities)),
                "ari_mean": stab["ari_mean"],
                "nmi_mean": stab["nmi_mean"],
            })

    return pd.DataFrame(rows).sort_values(["embedding_model", "block_size_sec"]).reset_index(drop=True)


def add_composite_score(df: pd.DataFrame) -> pd.DataFrame:
    out = df.copy()

    def mm(x):
        x = np.array(x, dtype=float)
        mn, mx = np.nanmin(x), np.nanmax(x)
        if np.isclose(mx, mn):
            return np.ones_like(x) * 0.5
        return (x - mn) / (mx - mn)

    outlier_bad = mm(out["outlier_ratio_mean"])
    nmi_good = mm(out["nmi_mean"].fillna(out["nmi_mean"].mean()))
    ari_good = mm(out["ari_mean"].fillna(out["ari_mean"].mean()))
    div_good = mm(out["topic_diversity_mean"])

    out["composite_score"] = (1.8 * (1 - outlier_bad)) + (1.0 * nmi_good) + (0.8 * ari_good) + (0.7 * div_good)
    return out


def pick_best_combo(scored_df: pd.DataFrame) -> Tuple[str, int]:
    best = scored_df.sort_values("composite_score", ascending=False).iloc[0]
    return str(best["embedding_model"]), int(best["block_size_sec"])


