def outlier_ratio(topics: List[int]) -> float:
    arr = np.array(topics)
    return float(np.mean(arr == -1)) if len(arr) else 1.0


def n_topics_effective(topics: List[int]) -> int:
    return len(set([t for t in topics if t != -1]))


def approx_topic_diversity(topic_model: BERTopic, topk: int = 10) -> float:
    topics_dict = topic_model.get_topics()
    words = []
    n_topics = 0
    for tid, pairs in topics_dict.items():
        if tid == -1:
            continue
        n_topics += 1
        words.extend([w for w, _ in pairs[:topk]])
    if n_topics == 0:
        return 0.0
    return len(set(words)) / float(topk * n_topics)


def stability_metrics(list_topics: List[List[int]]) -> Dict[str, float]:
    if len(list_topics) < 2:
        return {"ari_mean": np.nan, "nmi_mean": np.nan}
    aris, nmis = [], []
    for i in range(len(list_topics)):
        for j in range(i + 1, len(list_topics)):
            y1, y2 = np.array(list_topics[i]), np.array(list_topics[j])
            aris.append(adjusted_rand_score(y1, y2))
            nmis.append(normalized_mutual_info_score(y1, y2))
    return {"ari_mean": float(np.mean(aris)), "nmi_mean": float(np.mean(nmis))}


# ----------------------------
# Fit
# ----------------------------

def fit_with_outlier_reduction(
    docs: List[str],
    cfg: Config,
    embedding_model_name: str,
    run_id: int = 0,
    device: str = "cpu"
):
    model = build_topic_model(cfg, embedding_model_name, seed_shift=run_id, device=device)
    topics, probs = model.fit_transform(docs)

    # Réduction outliers robuste (fallback si erreur)
    try:
        new_topics = model.reduce_outliers(docs, topics, strategy="embeddings")
        model.update_topics(docs, topics=new_topics)
        topics = new_topics
    except Exception as e:
        print(f"[WARN] reduce_outliers a échoué ({embedding_model_name}, run {run_id}): {e}")

    return model, topics, probs


