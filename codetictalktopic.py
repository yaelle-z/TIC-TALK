def run_final_pipeline(cfg: Config):
    device = get_device()
    print(f"[INFO] Device détecté: {device}")
    print(f"[INFO] CUDA available: {torch.cuda.is_available()}")
    if torch.cuda.is_available():
        print(f"[INFO] GPU: {torch.cuda.get_device_name(0)}")

    # Traçabilité config
    with open(os.path.join(cfg.output_dir, "run_config.json"), "w", encoding="utf-8") as f:
        json.dump(asdict(cfg), f, indent=2, ensure_ascii=False)

    print("\n1) Benchmark croisé segmentation × embeddings")
    bench_df = benchmark_models_and_segmentations(cfg, device=device)
    scored = add_composite_score(bench_df)

    bench_csv = os.path.join(cfg.output_dir, "benchmark_models_segmentations.csv")
    scored.to_csv(bench_csv, index=False, encoding="utf-8")
    print(f"[OK] Benchmark: {bench_csv}")

    best_model, best_bs = pick_best_combo(scored)
    print(f"\n[SELECTED] model={best_model} | block_size={best_bs}s")

    print("\n2) Run final sur meilleur couple")
    df = load_corpus_with_provenance(cfg.input_srt_dir, block_size_sec=best_bs)
    docs = df["text"].tolist()

    set_all_seeds(cfg.random_state + 999)
    model, topics, probs = fit_with_outlier_reduction(
        docs, cfg, embedding_model_name=best_model, run_id=999, device=device
    )

    # Confidence robuste
    if isinstance(probs, np.ndarray) and probs.ndim == 2 and probs.shape[0] == len(docs):
        max_prob = np.max(probs, axis=1)
    else:
        max_prob = np.full(shape=(len(docs),), fill_value=np.nan)

    df["embedding_model"] = best_model
    df["topic"] = topics
    df["topic_confidence"] = max_prob
    df["is_low_conf"] = np.where(
        np.isnan(df["topic_confidence"]),
        0,
        (df["topic_confidence"] < cfg.low_conf_threshold).astype(int)
    )
    df["topic_label_for_analysis"] = df["topic"].astype(str)
    df.loc[df["is_low_conf"] == 1, "topic_label_for_analysis"] = "LOW_CONF"

    # 2bis) Export embeddings documentaires (recommandé)
    emb_npy = None
    emb_index_csv = None
    if cfg.export_doc_embeddings:
        print("\n3) Calcul et export des embeddings documentaires")
        embedder = SentenceTransformer(best_model, device=device)
        doc_embeddings = embedder.encode(
            docs,
            batch_size=cfg.embeddings_batch_size,
            show_progress_bar=True,
            convert_to_numpy=True,
            normalize_embeddings=cfg.normalize_embeddings
        )

        emb_npy = os.path.join(cfg.output_dir, "doc_embeddings.npy")
        np.save(emb_npy, doc_embeddings)

        emb_index_cols = ["show_id", "block_id", "start_sec", "end_sec", "text"]
        emb_index_csv = os.path.join(cfg.output_dir, "doc_embeddings_index.csv")
        df[emb_index_cols].to_csv(emb_index_csv, index=False, encoding="utf-8")

        # nettoyage mémoire
        del embedder, doc_embeddings
        gc.collect()
        if torch.cuda.is_available():
            torch.cuda.empty_cache()

    # Exports topics
    final_csv = os.path.join(cfg.output_dir, "segments_topics_global.csv")
    df.to_csv(final_csv, index=False, encoding="utf-8")

    per_show_dir = os.path.join(cfg.output_dir, "per_show_csv")
    os.makedirs(per_show_dir, exist_ok=True)
    for sid, g in df.groupby("show_id", sort=True):
        g.sort_values("block_id").to_csv(os.path.join(per_show_dir, f"{sid}_topics.csv"), index=False, encoding="utf-8")

    topic_info = model.get_topic_info()
    topic_info["embedding_model"] = best_model
    topic_info_csv = os.path.join(cfg.output_dir, "topic_info.csv")
    topic_info.to_csv(topic_info_csv, index=False, encoding="utf-8")

    topic_words = {}
    for tid in topic_info["Topic"].tolist():
        if tid == -1:
            continue
        topic_words[int(tid)] = model.get_topic(tid)[:cfg.top_n_words]
    topic_words_json = os.path.join(cfg.output_dir, "topic_top_words.json")
    with open(topic_words_json, "w", encoding="utf-8") as f:
        json.dump(topic_words, f, ensure_ascii=False, indent=2)

    model_dir = os.path.join(cfg.output_dir, "bertopic_model")
    model.save(
        model_dir,
        serialization="safetensors",
        save_ctfidf=True,
        save_embedding_model=cfg.save_embedding_model_in_bertopic
    )

    summary_txt = os.path.join(cfg.output_dir, "selection_summary.txt")
    top = scored.sort_values("composite_score", ascending=False).iloc[0]
    with open(summary_txt, "w", encoding="utf-8") as f:
        f.write(f"Device used: {device}\n")
        f.write(f"Best embedding model: {best_model}\n")
        f.write(f"Best block size (sec): {best_bs}\n")
        f.write(f"Embeddings exported: {cfg.export_doc_embeddings}\n")
        f.write(f"Embeddings normalized: {cfg.normalize_embeddings}\n")
        for col in ["outlier_ratio_mean", "n_topics_mean", "topic_diversity_mean", "ari_mean", "nmi_mean", "composite_score"]:
            f.write(f"- {col}: {top[col]}\n")

    print("\n[OK] Sorties générées")
    print(f"- {bench_csv}")
    print(f"- {final_csv}")
    print(f"- {topic_info_csv}")
    print(f"- {topic_words_json}")
    print(f"- {model_dir}")
    print(f"- {summary_txt}")
    if emb_npy:
        print(f"- {emb_npy}")
    if emb_index_csv:
        print(f"- {emb_index_csv}")


if __name__ == "__main__":
    srt_files = glob.glob(os.path.join(CFG.input_srt_dir, "*.srt"))
    if not srt_files:
        raise FileNotFoundError(
            f"Aucun .srt trouvé dans {CFG.input_srt_dir}. "
            "Ajoutez vos fichiers SRT puis relancez."
        )

    run_final_pipeline(CFG)