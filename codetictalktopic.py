def srt_time_to_seconds(t) -> float:
    return t.hours * 3600 + t.minutes * 60 + t.seconds + (t.milliseconds / 1000.0)


def normalize_text(text: str) -> str:
    text = re.sub(r"<[^>]+>", " ", text)
    text = text.replace("\n", " ")
    text = re.sub(r"\s+", " ", text).strip()
    return text


def read_srt_file(path: str) -> List[Dict]:
    subs = pysrt.open(path, encoding="utf-8", error_handling="ignore")
    rows = []
    for i, sub in enumerate(subs):
        txt = normalize_text(sub.text)
        if not txt:
            continue
        rows.append({
            "subtitle_idx": i,
            "start_sec": srt_time_to_seconds(sub.start),
            "end_sec": srt_time_to_seconds(sub.end),
            "text": txt
        })
    return rows


def build_blocks_from_subtitles(subs: List[Dict], block_size_sec: int) -> List[Dict]:
    if not subs:
        return []

    blocks = []
    block_end_target = subs[0]["start_sec"] + block_size_sec
    cur_texts = []
    cur_start = subs[0]["start_sec"]
    first_idx = subs[0]["subtitle_idx"]
    last_end = subs[0]["end_sec"]

    for row in subs:
        s, e, txt = row["start_sec"], row["end_sec"], row["text"]
        if s < block_end_target:
            cur_texts.append(txt)
            last_end = e
        else:
            merged = " ".join(cur_texts).strip()
            if merged:
                blocks.append({
                    "block_start_sec": cur_start,
                    "block_end_sec": last_end,
                    "text": merged,
                    "first_sub_idx": first_idx,
                    "last_sub_idx": row["subtitle_idx"] - 1
                })
            cur_texts = [txt]
            cur_start = s
            block_end_target = cur_start + block_size_sec
            first_idx = row["subtitle_idx"]
            last_end = e

    merged = " ".join(cur_texts).strip()
    if merged:
        blocks.append({
            "block_start_sec": cur_start,
            "block_end_sec": last_end,
            "text": merged,
            "first_sub_idx": first_idx,
            "last_sub_idx": subs[-1]["subtitle_idx"]
        })
    return blocks


def load_corpus_with_provenance(input_dir: str, block_size_sec: int) -> pd.DataFrame:
    files = sorted(glob.glob(os.path.join(input_dir, "*.srt")))
    if not files:
        raise FileNotFoundError(f"Aucun .srt dans: {input_dir}")

    all_rows = []
    for f in tqdm(files, desc=f"Lecture SRT ({block_size_sec}s)"):
        show_id = os.path.splitext(os.path.basename(f))[0]
        subs = read_srt_file(f)
        blocks = build_blocks_from_subtitles(subs, block_size_sec)
        for bi, b in enumerate(blocks):
            all_rows.append({
                "show_id": show_id,
                "block_id": bi,
                "block_size_sec": block_size_sec,
                "start_sec": b["block_start_sec"],
                "end_sec": b["block_end_sec"],
                "duration_sec": b["block_end_sec"] - b["block_start_sec"],
                "text": b["text"],
                "first_sub_idx": b["first_sub_idx"],
                "last_sub_idx": b["last_sub_idx"]
            })

    df = pd.DataFrame(all_rows)
    if df.empty:
        raise ValueError(f"Aucun bloc extrait depuis {input_dir}")
    return df


