def build_topic_model(cfg: Config, embedding_model_name: str, seed_shift: int = 0, device: str = "cpu") -> BERTopic:
    rs = cfg.random_state + seed_shift

    embedding_model = SentenceTransformer(embedding_model_name, device=device)

    umap_model = UMAP(
        n_neighbors=cfg.umap_n_neighbors,
        n_components=cfg.umap_n_components,
        min_dist=cfg.umap_min_dist,
        metric="cosine",
        random_state=rs
    )

    hdbscan_model = HDBSCAN(
        min_cluster_size=cfg.hdbscan_min_cluster_size,
        min_samples=cfg.hdbscan_min_samples,
        metric="euclidean",
        cluster_selection_method="eom",
        prediction_data=True
    )

    vectorizer_model = CountVectorizer(
        ngram_range=cfg.ngram_range,
        stop_words=cfg.language
    )

    topic_model = BERTopic(
        embedding_model=embedding_model,
        umap_model=umap_model,
        hdbscan_model=hdbscan_model,
        vectorizer_model=vectorizer_model,
        min_topic_size=cfg.min_topic_size,
        nr_topics="auto",
        calculate_probabilities=True,
        top_n_words=cfg.top_n_words,
        verbose=False
    )
    return topic_model


